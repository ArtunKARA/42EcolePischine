void	ft_rev_int_tab(int *tab, int size)
{
	int	c;
	int	lc;

	c = 0;
	while (c < size / 2)
	{
		lc = tab[c];
		tab[c] = tab[size - c - 1];
		tab[size - c - 1] = lc;
		c++;
	}
}
