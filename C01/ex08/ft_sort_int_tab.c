void	ft_sort_int_tab(int *tab, int size)
{
	int	c1;
	int	c2;
	int	rc;

	c1 = 0;
	while (c1 < size)
	{
		c2 = 0;
		while (c2 < size)
		{
			if (tab[c2] > tab[c1])
			{
				rc = tab[c1];
				tab[c1] = tab[c2];
				tab[c2] = rc;
			}
			c2++;
		}
		c1++;
	}
}
