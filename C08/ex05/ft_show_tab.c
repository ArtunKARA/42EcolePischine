#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putchr(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchr(str[i]);
		i++;
	}
	ft_putchr('\n');
}

void	ft_putnbr(int nb)
{
	if (nb >= 0)
	{
		if (nb > 9)
		{
			ft_putnbr(nb / 10);
			ft_putnbr(nb % 10);
		}
		else
			ft_putchr(nb + '0');
	}
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str)
	{
		ft_putstr(par[i].str);
		ft_putnbr(par[i].size);
		ft_putchr('\n');
		ft_putstr(par[i].copy);
		i++;
	}
}
