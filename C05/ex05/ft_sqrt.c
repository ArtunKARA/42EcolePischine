int	ft_sqrt(int nb)
{
	int	a;

	a = 0;
	if (nb <= 0)
		return (0);
	while (nb >= a * a && a <= 46341)
	{
		a++;
		if (nb == a * a)
			return (a);
	}
	return (0);
}
